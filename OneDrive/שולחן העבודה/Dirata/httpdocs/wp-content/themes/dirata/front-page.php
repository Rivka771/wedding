<script>
jQuery(document).ready(function($) {
  $('#front-page-search-form').on('submit', function(e) {
    e.preventDefault();

    var cityName = $('#city-input').val().trim();
    var streetName = $('#street-input').val().trim();
    var advanced = $('#advanced-select').val();

    var ajaxCity = $.ajax({
      url: dirataVars.ajaxurl,
      type: 'POST',
      data: {
        action: 'get_term_id_by_name',
        taxonomy: 'city',
        term_name: cityName
      },
      dataType: 'json'
    });

    var ajaxStreet = $.ajax({
      url: dirataVars.ajaxurl,
      type: 'POST',
      data: {
        action: 'get_term_id_by_name',
        taxonomy: 'street',
        term_name: streetName
      },
      dataType: 'json'
    });

    $.when(ajaxCity, ajaxStreet).done(function(cityResponse, streetResponse) {
      var cityId = "";
      var streetId = "";
      if (cityResponse[0].success && cityResponse[0].data.term_id) {
         cityId = cityResponse[0].data.term_id;
      }
      if (streetResponse[0].success && streetResponse[0].data.term_id) {
         streetId = streetResponse[0].data.term_id;
      }

      var baseUrl = "<?php echo site_url('/apartments-search/'); ?>";
      var params = new URLSearchParams();
      if(cityId !== "") {
         params.append('city', cityId);
      }
      if(streetId !== "") {
         params.append('street', streetId);
      }
      if(advanced !== "") {
         params.append('advanced', advanced);
      }

      window.location.href = baseUrl + "?" + params.toString();
    }).fail(function() {
      window.location.href = "<?php echo site_url('/apartments-search/'); ?>";
    });
  });
});

